// let e cont 
//let é para inserir uma variavel, dando a possibilidade de altera-la 
//const é para inserir uma variavel, mas não da a possibilidade de altera-la

//===================EXEMPLOS=======================

let meunome = 'Lucas Gabriel'
console.log("Meu nome é: ", meunome)
meunome = 'Lucas Silva'
console.log("Meu primeiro e ultimo nome é: ", meunome)